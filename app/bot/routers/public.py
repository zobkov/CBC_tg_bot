            "ü§ù <b>–§—É–Ω–∫—Ü–∏–∏ –≤–æ–ª–æ–Ω—Ç—ë—Ä–∞:</b>\n"
            "‚Ä¢ –ü–æ–º–æ—â—å –Ω–æ–≤—ã–º —É—á–∞—Å—Ç–Ω–∏–∫–∞–º\n"
